import UIKit

extension UIColor {
    static var lightGray: UIColor {
        return UIColor(red: 40/255, green: 40/255, blue: 40/255, alpha: 1.0)
    }

    static var defaultGray: UIColor {
        return UIColor(red: 36/255, green: 36/255, blue: 36/255, alpha: 1.0)
    }

    static var darkGray: UIColor {
        return UIColor(red: 30/255, green: 30/255, blue: 30/255, alpha: 1)
    }
}
